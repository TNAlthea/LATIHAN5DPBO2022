    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String nim = TextFieldNIM.getText();
        String nama = TextFieldNama.getText();
        String nilai = TextFieldNilai.getText();
        String prodi = TextFieldProdi.getText();
        
        if (nim.isEmpty() || nama.isEmpty() || nilai.isEmpty() || prodi.isEmpty()){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
        } else {
            if (isUpdate == false)
                insertData();
            else {
                updateData();
                addButton.setText("Add");
                deleteButton.setVisible(false);
                this.isUpdate = false;
            }
            clearText();
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void TabelHasilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelHasilMouseClicked
        // TODO add your handling code here:
        this.isUpdate = true;

        //Get Selected Data
        int row = TabelHasil.getSelectedRow();
        String selectedNim = (TabelHasil.getModel().getValueAt(row, 0).toString());
        String selectedNama = (TabelHasil.getModel().getValueAt(row, 1).toString());
        String selectedNilai = (TabelHasil.getModel().getValueAt(row, 2).toString());
        String selectedProdi = (TabelHasil.getModel().getValueAt(row, 3).toString());
        
        for (int i = 0; i < listMhs.size(); i++) {
            if (selectedNim.equals(listMhs.get(i).getNim())) {
                selectedID = i;
                break;
            }
        }

        //System.out.println(selectedID);
        TextFieldNIM.setText(selectedNim);
        TextFieldNama.setText(selectedNama);
        TextFieldNilai.setText(selectedNilai);
        TextFieldProdi.setText(selectedProdi);
        
        addButton.setText("Update");
        deleteButton.setVisible(true);
    }//GEN-LAST:event_TabelHasilMouseClicked

