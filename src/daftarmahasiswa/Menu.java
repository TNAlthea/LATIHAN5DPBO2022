    public final DefaultTableModel setTable() {
        Object[] column = {"NIM", "Nama", "Nilai", "Prodi"};
        DefaultTableModel dataTabel = new DefaultTableModel(null, column);
        for (int i = 0; i < listMhs.size(); i++) {
            Object[] row = new Object[4];
            row[0] = listMhs.get(i).getNim();
            row[1] = listMhs.get(i).getNama();
            row[2] = listMhs.get(i).getNilai();
            row[3] = listMhs.get(i).getProdi();
            dataTabel.addRow(row);
        }

        return dataTabel;
    }

    public void insertData() {
        // Get Data
        String nim = TextFieldNIM.getText();
        String nama = TextFieldNama.getText();
        String nilai = TextFieldNilai.getText();
        String prodi = TextFieldProdi.getText();
        
        listMhs.add(new Mahasiswa(nim, nama, nilai, prodi));
        //Update Table
        TabelHasil.setModel(setTable());

        System.out.println("Insert Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
    }

    public void updateData() {
        // Get Data
        String nim = TextFieldNIM.getText();
        String nama = TextFieldNama.getText();
        String nilai = TextFieldNilai.getText();
        String prodi = TextFieldProdi.getText();
        //System.out.println(nim + nama + nilai);

        listMhs.get(selectedID).setNim(nim);
        listMhs.get(selectedID).setNama(nama);
        listMhs.get(selectedID).setNilai(nilai);
        listMhs.get(selectedID).setProdi(prodi);
        //Update Table
        TabelHasil.setModel(setTable());
        
        System.out.println("Update Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
    }

    public void deleteData() {
        int i = 0;
        int found = 0;
        int index = 0;
        int record = listMhs.size();
        String nim = TextFieldNIM.getText();
        String nama = TextFieldNama.getText();
        String nilai = TextFieldNilai.getText();
        String prodi = TextFieldProdi.getText();
        while ((i < record) && (found == 0)){
            if((listMhs.get(i).getNim().equals(nim)) && (listMhs.get(i).getNama().equals(nama)) && (listMhs.get(i).getNilai().equals(nilai)) && (listMhs.get(i).getProdi().equals(prodi))) {
                found = 1;
                index = i;
            }
            i++;
        }
        
        if (found == 1) {
            ((DefaultTableModel)TabelHasil.getModel()).removeRow(index);
            listMhs.remove(index);
            System.out.println("Delete Success!");
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
        } else JOptionPane.showMessageDialog(null, "Data gagal dihapus karena textform sudah berubah!");
        
        
    }
    
